"""
PatchOps PR Template v2.1
Includes Supply Chain Audit and Smoke Test Results.
"""

def generate_pr_body(fixed_vulnerabilities_list, req_check_result=None, test_results=None):
    """
    Args:
        fixed_vulnerabilities_list (list): Array of vulnerability objects
        req_check_result (dict): Supply chain audit data
        test_results (list): Smoke test results
    """
    if not fixed_vulnerabilities_list:
        return "## 🛡️ PatchOps Security Update\nAutonomous fixes were applied to improve code quality."

    markdown = f"# 🛡️ Security Remediation Report: {len(fixed_vulnerabilities_list)} Critical Fixes\n\n"
    markdown += "This automated Pull Request applies surgical security patches generated by the **PatchOps Autonomous Pipeline**.\n\n"
    
    # 1. SUMMARY TABLE
    markdown += "## 📋 Executive Summary\n\n"
    markdown += "| Vulnerability | CWE | Severity | Status |\n"
    markdown += "| :--- | :--- | :--- | :--- |\n"
    for v in fixed_vulnerabilities_list:
        v_type = v.get('vulnerability_type', v.get('type', 'Security Flaw'))
        cwe = v.get('cwe', 'N/A')
        severity = v.get('severity', 'High')
        markdown += f"| {v_type} | {cwe} | {severity} | ✅ Resolved |\n"
    
    markdown += "\n---\n\n"

    # 2. SUPPLY CHAIN AUDIT
    if req_check_result:
        markdown += "## 🔍 Supply Chain Security Audit\n\n"
        markdown += f"**Scanned Files:** {', '.join(req_check_result.get('scanned_files', []))}  \n"
        markdown += f"**Packages Checked:** {len(req_check_result.get('all_packages', []))}  \n"
        markdown += f"**Overall Risk:** `{req_check_result.get('overall_risk', 'LOW')}`\n\n"
        
        flagged = req_check_result.get('flagged', [])
        if flagged:
            markdown += "### ⚠️ Flagged Packages\n\n"
            markdown += "| Package | Severity | Reason | Recommendation |\n"
            markdown += "| :--- | :--- | :--- | :--- |\n"
            for f in flagged:
                markdown += f"| {f['package']} | {f['severity']} | {f['reason']} | {f['recommendation']} |\n"
        else:
            markdown += "✅ All packages verified clean.\n\n"
        
        markdown += "---\n\n"

    # 3. SMOKE TEST RESULTS
    if test_results:
        passed = len([t for t in test_results if t['status'] == 'PASSED'])
        total = len(test_results)
        markdown += f"## 🧪 Smoke Test Results ({passed}/{total} Passed)\n\n"
        markdown += "| Test Case | Status | Duration |\n"
        markdown += "| :--- | :--- | :--- |\n"
        for t in test_results:
            icon = "✅" if t['status'] == 'PASSED' else "❌"
            markdown += f"| {t['name']} | {icon} {t['status']} | {t['duration_ms']}ms |\n"
        
        markdown += "\n---\n\n"

    # 4. REMEDIATION DETAILS
    markdown += "## 🔍 Detailed Remediation Logic\n\n"
    for i, v in enumerate(fixed_vulnerabilities_list, 1):
        v_type = v.get('vulnerability_type', v.get('type', 'Security Flaw'))
        cwe = v.get('cwe', 'N/A')
        explanation = v.get('explanation', 'Applied surgical patch to sanitize inputs.')
        
        markdown += f"### {i}. {v_type} ({cwe})\n"
        markdown += f"> [!IMPORTANT]\n"
        markdown += f"> {explanation}\n\n"
    
    markdown += "---  \n"
    markdown += "*Generated autonomously by PatchOps Security Pipeline.*"
    
    return markdown
